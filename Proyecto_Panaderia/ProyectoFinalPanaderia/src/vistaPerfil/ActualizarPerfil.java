    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        Perfil perfilModificado = new Perfil(Integer.valueOf(txtId.getText()),
                txtNombre.getText(),
                txtDescripcion.getText());

        if (txtNombre.getText().length() > 0 && txtDescripcion.getText().length() > 0) {
            if (controlador.ActualizarPerfil(perfilModificado)) {
                CrudPerfil newframe = new CrudPerfil();
                newframe.setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "error al actualizar");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Campos faltantes");
        }
    }//GEN-LAST:event_jbAceptarActionPerformed

